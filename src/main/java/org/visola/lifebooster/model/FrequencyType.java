package org.visola.lifebooster.model;

/**
 * Defines a type of frequency.
 */
public enum FrequencyType {

  /**
   * When frequency type is time, it's expressed in a time interval.
   * E.g.: 2 hours
   */
  TIME,

  /**
   * When frequency type is unit, it's expressed in a number of units.
   * E.g.: 2 times.
   */
  UNIT,
  ;

}
